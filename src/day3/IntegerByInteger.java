package day3;

public class IntegerByInteger
{
    public static void main(String[] args) {
        //1. I/I = I part only
        System.out.println("************I/I = I part only=************I");
        int i = 10;
        int j = 2;
        int divide = i/j;
        System.out.println(divide);

        int k = 9;
        int divide2=k/j; //9/2 = 4.5
        System.out.println(divide2); //4

        int l = 7;
        int divide3=l/j; //7/2 = 3.5
        System.out.println(divide3);// 3

        System.out.println("====Examples=======");
        System.out.println(10/2);//5
        System.out.println(10/3);//3
        System.out.println(9/2); //4
        System.out.println(9/3); //3
        System.out.println(7/3); //2





    }


}
