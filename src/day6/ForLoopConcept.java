package day6;

public class ForLoopConcept
{
    public static void main(String[] args) {

        System.out.println("==========1. WAP to print my name n number of times================");

        for (int i =1;i<=10;i++)
        {
            System.out.println("Ashish");

        }

        System.out.println("==========2. WAP to print my numbers from 1 to 10================");


        for (int j =1;j<=10;j++)
        {
            System.out.println(j);
        }

        System.out.println("==========3. WAP to print my numbers from 10 to 1================");


        for (int k=10;k>0;k--)
        {
            System.out.println(k);

        }

        System.out.println("==========4. WAP to print table of 2================");
        for (int m = 1;m<=10;m++)
        {
            System.out.println(2*m);

        }
    }
}
